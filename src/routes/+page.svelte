            />
        </a>
    </div>
    <p>Click on the Tauri, Vite, and SvelteKit logos to learn more.</p>

    <form class="row" onsubmit={greet}>
        <input
            id="greet-input"
            placeholder="Enter a name..."
            bind:value={name}
        />
        <button class="btn-primary" type="submit">Greet</button>
    </form>
    <p>{greetMsg}</p>
</main>

